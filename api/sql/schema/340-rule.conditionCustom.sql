CREATE TYPE [rule].conditionCustom AS TABLE (
    conditionId INT,
    conditionName NVARCHAR(100),
    name NVARCHAR(100),
    description NVARCHAR(100),
    notes NVARCHAR(1000),
    priority INT,
	operationStartDate DATETIME,
	operationEndDate DATETIME,
    sourceAccountId NVARCHAR(255),
	destinationAccountId NVARCHAR(255),	
	createdBy BIGINT,
    -- conditionActor:
    channelOrganization VARCHAR(8000),
    holderOrganization VARCHAR(8000),
    counterpartyOrganization VARCHAR(8000),
    -- conditionItem
    operation VARCHAR(8000),
    channelCountry VARCHAR(8000),
    channelRegion VARCHAR(8000),
    channelCity VARCHAR(8000),
    holderCountry VARCHAR(8000),
    holderRegion VARCHAR(8000),
    holderCity VARCHAR(8000),
    counterpartyCountry VARCHAR(8000),
    counterpartyRegion VARCHAR(8000),
    counterpartyCity VARCHAR(8000),
    holderCardProduct VARCHAR(8000),
    holderAccountProduct VARCHAR(8000),
    counterpartyCardProduct VARCHAR(8000),
    counterpartyAccountProduct VARCHAR(8000),
    -- conditionProperty:
    operationTag VARCHAR(8000),
    channelOrganizationTag VARCHAR(8000),
    holderOrganizationTag VARCHAR(8000),
    counterpartyOrganizationTag VARCHAR(8000),
    holderKyc VARCHAR(8000),
    holderCustomerType VARCHAR(8000),
    counterpartyKyc VARCHAR(8000),
    counterpartyCustomerType VARCHAR(8000)
)
